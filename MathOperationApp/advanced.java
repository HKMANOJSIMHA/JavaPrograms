package advancedArithematic;

import java.lang.Math;

public class advanced {
	
	public double sine(double input) {
		double rad= Math.toRadians(input);
		return Math.sin(rad);
	}
	
	
	public double absloute(double input) {
		return Math.abs(input);
	}
	
	
	public double logBase10(double input) {
		return Math.log10(input);
	}

}
